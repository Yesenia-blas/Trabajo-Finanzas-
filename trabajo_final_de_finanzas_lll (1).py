# C√°lculo de d1 y d2
d1_bs = (np.log(S_bs / K_bs) + (r_bs + sigma_bs**2 / 2) * T_bs) / (sigma_bs * np.sqrt(T_bs))
d2_bs = d1_bs - sigma_bs * np.sqrt(T_bs)

# Precio de la opci√≥n call
C_bs = S_bs * norm.cdf(d1_bs) - K_bs * np.exp(-r_bs * T_bs) * norm.cdf(d2_bs)

